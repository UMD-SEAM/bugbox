<h1  style="visibility:visible;">Newsletter Manager (V 1.0)</h1>
Create and send html or plain text email newsletters to your subscribers. The plugin supports unlimited email campaigns, unlimited email addresses,  double opt-in anti-spam compliance, hourly email sending limit and much more. Opt-in form is available as HTML code, shortcode as well as standard Wordpress widget. The import/export tool allows to create and restore backup of your subscriber list. Newsletter Manager is developed and maintained by <a target="_blank" href="http://xyzscripts.com">xyzscripts</a>. 

   
